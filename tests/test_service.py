@pytest.mark.asyncio
async def test_service_submits_host_commit(tmp_path: Path) -> None:
    initialize_repository(tmp_path)
    config = make_config(tmp_path)
    service = AgentBraidService(
        config, tmp_path, codex=FakePlanner(host_plan(mutates_workspace=True))
    )
    run = await service.start_run(StartRunRequest(goal="Edit and commit."))
    claimed = service.claim_host_task(run.run_id, "host-session")
    assert claimed is not None
    assert claimed.worktree_path is not None
    worktree = Path(claimed.worktree_path)
    (worktree / "host.txt").write_text("host change\n", encoding="utf-8")
    git(worktree, "add", "host.txt")
    git(worktree, "commit", "--signoff", "-m", "feat: add host change")
    commit_sha = git(worktree, "rev-parse", "HEAD")

    completed = await service.submit_host_result(
        run.run_id,
        "host-task",
        "host-session",
        HostTaskResult(
            outcome=TaskOutcome.SUCCEEDED,
            summary="Change validated and committed.",
            changed_files=["host.txt"],
            validations=[ValidationEvidence(command="test -f host.txt", passed=True)],
            commit_sha=commit_sha,
        ),
    )

    assert completed.status == TaskStatus.SUCCEEDED
    assert completed.commit_sha == commit_sha
    assert service.get_run(run.run_id).status == RunStatus.COMPLETED
    integration_path = service.worktrees.integration_path(run.run_id)
    assert (integration_path / "host.txt").read_text(encoding="utf-8") == "host change\n"
    assert not (tmp_path / "host.txt").exists()
    host_capability = next(
        item for item in service.list_capabilities() if item.executor == Executor.HOST
    )
    assert host_capability.successes == 1


@pytest.mark.asyncio
async def test_service_executes_and_integrates_codex_worker(tmp_path: Path) -> None:
    initialize_repository(tmp_path)
    config = make_config(tmp_path)
    codex = FakePlanner(codex_plan(), worker_writes=True)
    service = AgentBraidService(config, tmp_path, codex=codex)

    run = await service.start_run(StartRunRequest(goal="Generate the requested file."))

    assert run.status == RunStatus.COMPLETED
    assert run.tasks[0].status == TaskStatus.SUCCEEDED
    assert run.tasks[0].commit_sha is not None
    assert run.tasks[0].worktree_path is not None
    assert [task.task_id for task in codex.worker_tasks] == ["codex-task"]
    assert codex.reviewed_runs == [run.run_id]
    integration_path = service.worktrees.integration_path(run.run_id)
    assert (integration_path / "generated.txt").read_text(encoding="utf-8") == (
        "generated by codex\n"
    )
    assert not (tmp_path / "generated.txt").exists()

    applied = service.apply_run(run.run_id)

    assert applied.commit_sha == git(tmp_path, "rev-parse", "HEAD")
    assert (tmp_path / "generated.txt").read_text(encoding="utf-8") == "generated by codex\n"


@pytest.mark.asyncio
async def test_unsigned_host_commit_is_released_for_retry(tmp_path: Path) -> None:
    initialize_repository(tmp_path)
    config = make_config(tmp_path)
    service = AgentBraidService(
        config,
        tmp_path,
        codex=FakePlanner(host_plan(mutates_workspace=True)),
    )
    run = await service.start_run(StartRunRequest(goal="Try an unsigned host change."))
    claimed = service.claim_host_task(run.run_id, "host-session")
    assert claimed is not None
    assert claimed.worktree_path is not None
    worktree = Path(claimed.worktree_path)
    (worktree / "unsigned.txt").write_text("unsigned\n", encoding="utf-8")
    git(worktree, "add", "unsigned.txt")
    git(worktree, "commit", "-m", "feat: unsigned change")
    commit_sha = git(worktree, "rev-parse", "HEAD")

    retryable = await service.submit_host_result(
        run.run_id,
        "host-task",
        "host-session",
        HostTaskResult(
            outcome=TaskOutcome.SUCCEEDED,
            summary="Submitted without a signoff.",
            changed_files=["unsigned.txt"],
            validations=[ValidationEvidence(command="test -f unsigned.txt", passed=True)],
            commit_sha=commit_sha,
        ),
    )

    assert retryable.status == TaskStatus.READY
    assert retryable.result is not None
    assert retryable.result.outcome == TaskOutcome.FAILED
    reclaimed = service.claim_host_task(run.run_id, "host-session")
    assert reclaimed is not None
    assert reclaimed.commit_sha is None


