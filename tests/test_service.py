class FakePlanner:
    def __init__(
        self,
        plan: RunPlan,
        *,
        worker_writes: bool = False,
        review_approved: bool = True,
    ) -> None:
        self.output_plan = plan
        self.worker_writes = worker_writes
        self.review_approved = review_approved
        self.requests: list[StartRunRequest] = []
        self.worker_tasks: list[TaskSpec] = []
        self.reviewed_runs: list[str] = []

    async def plan(
        self,
        request: StartRunRequest,
        workspace: Path,
    ) -> StructuredProviderResult[RunPlan]:
        self.requests.append(request)
        return StructuredProviderResult(
            output=self.output_plan,
            thread_id="lead-thread",
            usage=ProviderUsage(input_tokens=100, output_tokens=20),
            events=(),
            duration_seconds=1.5,
        )

    async def execute_task(
        self,
        task: TaskSpec,
        workspace: Path,
        *,
        run_id: str,
    ) -> StructuredProviderResult[WorkerResult]:
        del run_id
        self.worker_tasks.append(task)
        if not self.worker_writes:
            raise AssertionError("host-only test unexpectedly invoked a Codex worker")
        (workspace / "generated.txt").write_text("generated by codex\n", encoding="utf-8")
        return StructuredProviderResult(
            output=WorkerResult(
                outcome=TaskOutcome.SUCCEEDED,
                summary="Generated and validated the requested file.",
                changed_files=["generated.txt"],
                validations=[ValidationEvidence(command="test -f generated.txt", passed=True)],
                confidence=0.95,
            ),
            thread_id="worker-thread",
            usage=ProviderUsage(input_tokens=50, output_tokens=10),
            events=(),
            duration_seconds=0.5,
        )

    async def review_run(
        self,
        run: RunSnapshot,
        workspace: Path,
    ) -> StructuredProviderResult[RunReview]:
        assert workspace.is_dir()
        self.reviewed_runs.append(run.run_id)
        return StructuredProviderResult(
            output=RunReview(
                approved=self.review_approved,
                summary=(
                    "The integrated candidate meets all acceptance criteria."
                    if self.review_approved
                    else "The integrated candidate needs maintainer review."
                ),
                validations=[ValidationEvidence(command="git diff --check HEAD^", passed=True)],
            ),
            thread_id="lead-thread",
            usage=ProviderUsage(input_tokens=75, output_tokens=15),
            events=(),
            duration_seconds=0.75,
        )


class SequencedPlanner(FakePlanner):
    async def execute_task(
        self,
        task: TaskSpec,
        workspace: Path,
        *,
        run_id: str,
    ) -> StructuredProviderResult[WorkerResult]:
        del run_id
        self.worker_tasks.append(task)
        filename = f"{task.task_id}.txt"
        (workspace / filename).write_text(f"{task.task_id}\n", encoding="utf-8")
        return StructuredProviderResult(
            output=WorkerResult(
                outcome=TaskOutcome.SUCCEEDED,
                summary=f"Completed {task.task_id}.",
                changed_files=[filename],
                validations=[ValidationEvidence(command=f"test -f {filename}", passed=True)],
            ),
            thread_id=f"worker-{task.task_id}",
            usage=ProviderUsage(input_tokens=25, output_tokens=5),
            events=(),
            duration_seconds=0.25,
        )


def make_config(tmp_path: Path) -> AgentBraidConfig:
    state_dir = tmp_path.parent / f"{tmp_path.name}-state"
    return AgentBraidConfig(
        state_dir=state_dir,
        database_path=state_dir / "agentbraid.db",
        worktree_dir=state_dir / "worktrees",
        codex_model="gpt-test",
    )


def git(repository: Path, *arguments: str) -> str:
    completed = subprocess.run(
        ["git", "-C", str(repository), *arguments],
        capture_output=True,
        check=False,
        text=True,
    )
    if completed.returncode != 0:
        raise AssertionError(completed.stderr)
    return completed.stdout.strip()


def initialize_repository(workspace: Path) -> None:
    git(workspace, "init", "-b", "main")
    git(workspace, "config", "user.name", "AgentBraid Test")
    git(workspace, "config", "user.email", "agentbraid@example.test")
    (workspace / "README.md").write_text("# Test\n", encoding="utf-8")
    git(workspace, "add", "README.md")
    git(workspace, "commit", "--signoff", "-m", "chore: initialize test repository")


def host_plan(*, mutates_workspace: bool = False) -> RunPlan:
    return RunPlan(
        summary="Delegate one bounded task to the active host.",
        tasks=[
            TaskSpec(
                task_id="host-task",
                title="Complete host task",
                instructions="Complete and validate the bounded host task.",
                kind=TaskKind.IMPLEMENTATION,
                preferred_executor=Executor.HOST,
                mutates_workspace=mutates_workspace,
                acceptance_criteria=["The task is validated."],
            )
        ],
        final_acceptance_criteria=["The host task succeeds."],
    )


def codex_plan() -> RunPlan:
    return RunPlan(
        summary="Assign one isolated implementation task to Codex.",
        tasks=[
            TaskSpec(
                task_id="codex-task",
                title="Generate file",
                instructions="Generate and validate generated.txt.",
                kind=TaskKind.IMPLEMENTATION,
                preferred_executor=Executor.CODEX,
                mutates_workspace=True,
                acceptance_criteria=["generated.txt exists."],
            )
        ],
        final_acceptance_criteria=["The Codex change is integrated."],
    )


def dependent_codex_plan() -> RunPlan:
    child = TaskSpec(
        task_id="child",
        title="Complete child",
        instructions="Complete the child after its parent.",
        kind=TaskKind.IMPLEMENTATION,
        preferred_executor=Executor.CODEX,
        mutates_workspace=True,
        dependencies=["parent"],
        acceptance_criteria=["child.txt exists."],
    )
    parent = TaskSpec(
        task_id="parent",
        title="Complete parent",
        instructions="Complete the parent first.",
        kind=TaskKind.IMPLEMENTATION,
        preferred_executor=Executor.CODEX,
        mutates_workspace=True,
        acceptance_criteria=["parent.txt exists."],
    )
    return RunPlan(
        summary="Execute a parent-child mutation chain.",
        tasks=[child, parent],
        final_acceptance_criteria=["Both commits are integrated in dependency order."],
    )


@pytest.mark.asyncio
async def test_service_starts_run_and_tracks_host_capability(tmp_path: Path) -> None:
    initialize_repository(tmp_path)
    config = make_config(tmp_path)
    store = StateStore(config.database_path)
    planner = FakePlanner(host_plan())
    service = AgentBraidService(config, tmp_path, store=store, codex=planner)

    run = await service.start_run(
        StartRunRequest(goal="Complete the feature.", host_model="antigravity-test")
    )
    claimed = service.claim_host_task(run.run_id, "host-session")

    assert run.status == RunStatus.RUNNING
    assert run.request.workspace == str(tmp_path)
    assert run.lead_thread_id == "lead-thread"
    assert run.tasks[0].status == TaskStatus.READY
    assert claimed is not None
    assert claimed.status == TaskStatus.RUNNING
    capabilities = service.list_capabilities()
    assert [(item.executor, item.model) for item in capabilities] == [
        (Executor.CODEX, "gpt-test"),
        (Executor.HOST, "antigravity-test"),
    ]


