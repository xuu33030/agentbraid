@pytest.mark.asyncio
async def test_codex_commits_integrate_in_dependency_order(tmp_path: Path) -> None:
    initialize_repository(tmp_path)
    config = make_config(tmp_path)
    codex = SequencedPlanner(dependent_codex_plan(), worker_writes=True)
    service = AgentBraidService(config, tmp_path, codex=codex)

    run = await service.start_run(StartRunRequest(goal="Execute the dependency chain."))

    assert run.status == RunStatus.COMPLETED
    assert [task.spec.task_id for task in run.tasks] == ["child", "parent"]
    assert all(task.status == TaskStatus.SUCCEEDED for task in run.tasks)
    assert [task.task_id for task in codex.worker_tasks] == ["parent", "child"]
    integration_path = service.worktrees.integration_path(run.run_id)
    subjects = git(integration_path, "log", "--reverse", "--format=%s").splitlines()
    parent_index = next(
        index for index, subject in enumerate(subjects) if "agentbraid(parent)" in subject
    )
    child_index = next(
        index for index, subject in enumerate(subjects) if "agentbraid(child)" in subject
    )
    assert parent_index < child_index
